def calculate_average(numbers):
    """
    计算数字列表的平均值
    
    参数:
        numbers: 数字列表
    
    返回:
        平均值
    """
    # 提示：考虑除数为 0 的情况
    total = sum(numbers)
    return total / len(numbers)



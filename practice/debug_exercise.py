def find_max_value(data_list):
    """
    查找列表中的最大值
    
    参数:
        data_list: 包含数字的列表
    
    返回:
        最大值
    """
    # 提示：没有检查空列表的情况
    max_val = data_list[0]
    for item in data_list:
        if item > max_val:
            max_val = item
    return max_val



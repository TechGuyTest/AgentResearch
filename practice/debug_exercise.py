def remove_duplicates(items):
    """
    移除列表中的重复项
    
    参数:
        items: 原始列表
    
    返回:
        去重后的列表
    """
    result = []
    for item in items:
        # 提示：这个检查在每次迭代时做了什么？
        if item not in result:
            result.append(item)
    return result


